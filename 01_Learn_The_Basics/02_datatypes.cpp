#include <iostream>
using namespace std;

/*
========================
      DATATYPES
========================
*/

int main() {

    /*
        Primitive Datatypes in C++
        ---------------------------
        int     -> whole numbers
        float   -> decimal (6-7 digits precision)
        double  -> decimal (15+ digits precision)
        char    -> single character
        bool    -> true/false
    */

    int a = 10;
    float b = 2.5f;
    double c = 3.1415926535;
    char d = 'X';
    bool e = true;

    cout << "int: " << a << endl;
    cout << "float: " << b << endl;
    cout << "double: " << c << endl;
    cout << "char: " << d << endl;
    cout << "bool: " << e << endl;

    return 0;
}