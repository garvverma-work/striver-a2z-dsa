#include <iostream>
using namespace std;

/*
========================
        VARIABLES
========================
*/

int main() {

    // A variable is a named storage for data

    int age = 20;              // integer variable
    float height = 5.9f;       // decimal (less precision)
    double pi = 3.1415926535;  // high precision decimal
    char grade = 'A';          // single character
    bool isStudent = true;     // true / false
    string name = "Garv";      // text (string)

    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;

    return 0;
}